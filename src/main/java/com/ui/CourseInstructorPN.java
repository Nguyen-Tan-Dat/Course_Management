    public void docDSCI() {
        CourseInstructorBUS bus = new CourseInstructorBUS();
        if(CourseInstructorBUS.dsci == null);
              bus.docSDCI();
              
        Vector header = new Vector();
        header.add("CourseID");
        header.add("PersonID");
        model = new DefaultTableModel(header, 0);
        for(CourseInstructorDTO ci : CourseInstructorBUS.dsci){
            Vector row = new Vector();
            row.add(ci.getCourseID());
            row.add(ci.getPersonID());
            model.addRow(row);
        }        
        tblCourseInstructor.setModel(model);
    }

