    private void btTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTimkiemActionPerformed
        // TODO add your handling code here:
        int index = cbCourseInstructor.getSelectedIndex();
        String in = inputTK.getText();
        if(index == 0)
            tblCourseInstructor.setModel(ketQuaTimKiem(bus.timTheoCourseID(Integer.parseInt(in))));
        else if (index == 1)
            tblCourseInstructor.setModel(ketQuaTimKiem(bus.timTheoPersonID(Integer.parseInt(in))));
        else
            tblCourseInstructor.setModel(ketQuaTimKiem(bus.timTheoTatCa(Integer.parseInt(in))));
    }//GEN-LAST:event_btTimkiemActionPerformed

    private DefaultTableModel ketQuaTimKiem(ArrayList<CourseInstructorDTO> rs){
        Vector header = new Vector();
        header.add("CourseID");
        header.add("PersonID");
        model = new DefaultTableModel(header, 0);
        for(CourseInstructorDTO ci : rs){
            Vector row = new Vector();
            row.add(ci.getCourseID());
            row.add(ci.getPersonID());
            model.addRow(row);
        }
        return model;
    }
